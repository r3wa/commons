package br.com.r3wa.commons.lang;

public class R3WACommonsLangException extends RuntimeException{


	private static final long serialVersionUID = 1L;


	public R3WACommonsLangException() {
		super();
	}


	public R3WACommonsLangException(String message) {
		super(message);
	}


	public R3WACommonsLangException(String message, Throwable cause) {
		super(message, cause);
	}



}
